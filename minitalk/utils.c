#include "minitalk.h"

void	red(void)
{
	ft_putstr("\033[1;31m");
}

void	ft_error(char *str)
{
	red();
	ft_putstr(str);
	exit(1);
}

int	ft_atoi(const char *str)
{
	int	b;
	int	res;
	int	i;

	i = 0;
	b = 1;
	res = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' || str[i] == '\v'
		|| str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			b = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + str[i] - '0';
		i++;
	}
	return (res * b);
}

void	ft_putnbr(int nb)
{
	long long int	nbr;

	nbr = nb;
	if (nbr < 0)
	{
		ft_putchar('-');
		nbr = -nbr;
	}
	if (nbr > 9)
	{
		ft_putnbr(nbr / 10);
		ft_putchar((nbr % 10) + '0');
	}
	else
		ft_putchar(nbr + '0');
}

void	ft_putchar(char c)
{
	write(1, &c, 1);
}
